def split_text(text: str, limit: int = 3300) -> list[str]:
    parts = []
    current = ""

    for block in text.split("\n\n"):
        block = block.strip()
        if not block:
            continue

        candidate = f"{current}\n\n{block}".strip() if current else block

        if len(candidate) <= limit:
            current = candidate
        else:
            if current:
                parts.append(current)

            while len(block) > limit:
                parts.append(block[:limit])
                block = block[limit:]

            current = block

    if current:
        parts.append(current)

    return parts


def legal_keyboard(doc_type: str, page: int, total_pages: int):
    builder = InlineKeyboardBuilder()

    if page > 0:
        builder.button(text="⬅️ Назад", callback_data=f"legal:{doc_type}:{page - 1}")

    if page < total_pages - 1:
        builder.button(text="➡️ Далее", callback_data=f"legal:{doc_type}:{page + 1}")

    builder.button(text="🔙 Назад", callback_data="other")
    builder.button(text="🏠 В меню", callback_data="main_menu")

    if total_pages > 1:
        builder.adjust(2, 1, 1)
    else:
        builder.adjust(1, 1)

    return builder.as_markup()


@router.callback_query(F.data == "main_menu")
async def main_menu_callback(callback: CallbackQuery):
    await callback.message.edit_text(
        text="🏠 <b>Главное меню</b>\n\nВыберите раздел:",
        reply_markup=main_menu()
    )
    await callback.answer()


