@router.callback_query(F.data.startswith("legal:"))
async def legal_callback_handler(callback: CallbackQuery):
    _, doc_type, page_raw = callback.data.split(":", 2)

    if doc_type not in LEGAL_TEXTS:
        await callback.answer("Документ не найден", show_alert=True)
        return

    pages = split_text(LEGAL_TEXTS[doc_type]["text"])
    page = int(page_raw)

    if page < 0 or page >= len(pages):
        page = 0

    title = LEGAL_TEXTS[doc_type]["title"]
    page_info = f"\n\nСтраница {page + 1}/{len(pages)}" if len(pages) > 1 else ""

    await callback.message.edit_text(
        text=f"📄 <b>{title}</b>\n\n{pages[page]}{page_info}",
        reply_markup=legal_keyboard(doc_type, page, len(pages))
    )
    await callback.answer()
